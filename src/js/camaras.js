function generarCamaras() {
  const contenedor = document.getElementById("accordionCameras");
  contenedor.innerHTML = ""; // limpia contenido anterior si existe

  camaras
    .filter((camara) => camara.categoria === categoriaActual)
    .forEach((camara) => {
      const item = document.createElement("div");
      item.className = "accordion-item shadow-sm mb-3 rounded";
      item.innerHTML = `
        <h2 class="accordion-header" id="heading${camara.id}">
          <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapse${camara.id}" aria-expanded="false" aria-controls="collapse${camara.id}">
            <img src="${camara.imagen}" alt="${camara.modelo}" class="img-fluid rounded me-3" style="width: 100px; height: 80px; object-fit: cover;">
            <div class="flex-grow-1">
              <h5 class="mb-0">${camara.marca + " " + camara.modelo} <span class="badge bg-secondary ms-2">${camara.fechaLanzamiento}</span></h5>
              <small class="text-muted d-block">${camara.descripcionCorta}</small>
            </div>
          </button>
        </h2>
        <div id="collapse${camara.id}" class="accordion-collapse collapse" aria-labelledby="heading${camara.id}" data-bs-parent="#accordionCameras">
          <div class="accordion-body">
            <h6>Características principales:</h6>
            <ul>${camara.caracteristicas.map((c) => `<li>${c}</li>`).join("")}</ul>
            <p>${camara.descripcionCompleta}</p>
            <a href="${camara.link}" class="btn btn-dark mt-3">Seguir leyendo</a>
          </div>
        </div>
      `;
      contenedor.appendChild(item);
    });
}





document.addEventListener("DOMContentLoaded", () => {
  generarCamaras();

});



